// Step 1: Create a variable named price
let price = "250";

// Step 2: Convert price using parseInt()
let priceAsInt = parseInt(price);
console.log(priceAsInt); // Output: 250

// Step 3: Convert price using Number()
let priceAsNumber = Number(price);
console.log(priceAsNumber); // Output: 250

// Step 4: Convert price using the + operator
let priceWithPlus = +price;
console.log(priceWithPlus); // Output: 250

// Step 1: Create a variable named distance
let distance = 200;

// Step 2: Convert distance using .toString()
let distanceAsString = distance.toString();
console.log(distanceAsString); // Output: "200"

// Step 3: Convert distance using String()
let distanceWithString = String(distance);
console.log(distanceWithString); // Output: "200"

